#!/usr/bin/env bash
# ==============================================================================
# HAT Benchmark — 15 Datasets
# ==============================================================================

set -euo pipefail

cd "$(dirname "$0")"

if [[ ! -f hat_new.py ]]; then
  echo "error cannot find hat_new.py" >&2
  exit 1
fi

mkdir -p log

echo "========================================"
echo "SpMHGAT Benchmark - 15 Datasets"
echo "Repo: $(pwd)"
echo "Start: $(date '+%Y-%m-%d %H:%M:%S')"
echo "========================================"


COMMON=(
  -gpu 0
  -lr 0.005
  -l2 0.0001
  -units 8
  -heads 1
  -drop 0.2
  -c 1
  --nb_epochs 100000
  --patience 100
  --min_delta_loss 0.0001
  --n_runs 5
  --seed 66
)

run_one() {
  local name="$1"
  shift
  echo ""
  echo "[${name}] $(date '+%H:%M:%S')"
  python hat_new.py -dataset "${name}" "${COMMON[@]}" "$@" 2>&1 | tee "log/${name}.log"
  echo "[${name}] done -> log/${name}.log"
}

# ==============================================================================
# 1. my_actor
# ==============================================================================
run_one my_actor

# ==============================================================================
# 2. airport
# ==============================================================================
run_one airport

# ==============================================================================
# 3. citeseer
# ==============================================================================
run_one citeseer

# ==============================================================================
# 4. cora
# ==============================================================================
run_one cora

# ==============================================================================
# 5. my_cornell
# ==============================================================================
run_one my_cornell

# ==============================================================================
# 6. disease
# ==============================================================================
run_one disease

# ==============================================================================
# 7. carcinogenesis
# ==============================================================================
run_one carcinogenesis

# ==============================================================================
# 8. f1（ balanced CE）
# ==============================================================================
run_one f1 --feat_preproc standardize_train --balanced_loss

# ==============================================================================
# 9. hepatitis_std
# ==============================================================================
run_one hepatitis_std

# ==============================================================================
# 10. pubmed
# ==============================================================================
run_one pubmed

# ==============================================================================
# 11. hockey
# ==============================================================================
run_one hockey

# ==============================================================================
# 12. pte
# ==============================================================================
run_one pte

# ==============================================================================
# 13. toxicology
# ==============================================================================
run_one toxicology

# ==============================================================================
# 14. my_telecom
# ==============================================================================
run_one my_telecom

# ==============================================================================
# 15. cs_phds_nc
# ==============================================================================
run_one cs_phds_nc

echo ""
echo "========================================"
echo "hat.sh done"
echo "End: $(date '+%Y-%m-%d %H:%M:%S')"
echo "log: ./log/<dataset>.log"
echo "========================================"
